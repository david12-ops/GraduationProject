import Head from 'next/head';
import { useEffect, useState } from 'react';

import { useSuppDataQuery } from '@/generated/graphql';

import styles from '../styles/Home.module.css';
import { MediaCard } from './components/Cards/SuppCards';
import { Navbar } from './components/navbar2';

// pouziti sorting
const IsThereSupp = (data: any) => {
  console.log('co kontrolujeme', data);
  // eslint-disable-next-line sonarjs/prefer-single-boolean-return
  if (data) {
    return true;
  }
  return false;
};

const PageBody = (warning: any, dataSupp: any) => {
  if (!warning) {
    return (
      <div
        style={{
          textAlign: 'center',
          color: 'orange',
          fontSize: '30px',
          fontWeight: 'bold',
        }}
      >
        Nejsou dodavatel√©
      </div>
    );
  }

  return (
    <div>
      {dataSupp?.suplierData?.map((item: any) => (
        <MediaCard
          key={item.supplierId}
          packInBox={item.packInBox}
          name={item.suppName}
          sendCash={item.sendCashDelivery}
          folie={item.foil}
          shippingLabel={item.shippingLabel}
          pickUp={item.pickUp}
          delivery={item.delivery}
          insurance={item.insurance}
          suppId={item.supplierId}
        />
      ))}
    </div>
  );
};

// eslint-disable-next-line import/no-default-export
export default function SuppCards() {
  const suppData = useSuppDataQuery();
  const [body, SetBody] = useState({});

  useEffect(() => {
    if (!suppData.loading) {
      const errSup = IsThereSupp(suppData);

      SetBody({
        data: PageBody(errSup, suppData.data),
      });
    }
  }, [suppData]);

  return (
    <div className={styles.container}>
      <Head>
        <title>Details</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <Navbar />
      <main className={styles.main}>
        <h1 style={{ textAlign: 'center' }}>Welcome to detail of suppliers</h1>
        {body.data}
      </main>

      <footer className={styles.footer}></footer>
    </div>
  );
}
